//unit impulse signal
void unit_impulse(double unit_impulse_arr[], const size_t unit_impulse_len)
{
    unit_impulse_arr[0] = 1;
    for(size_t n = 1; n < unit_impulse_len; n++)
    {
        unit_impulse_arr[n] = 0;
   }
}


//unit step signal
void unit_step(double unit_step_arr[], const size_t unit_step_len)
{
    for(size_t n = 0; n < unit_step_len; n++)
    {
        unit_step_arr[n] = 1;
   }
}




